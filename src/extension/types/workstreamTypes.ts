/**
 * Workstream Map data model.
 * Defines all types for the subway-map style project visualization.
 */

// --- Core enums ---

export type WorkstreamType =
  | 'feature'
  | 'bug_fix'
  | 'research'
  | 'refactor'
  | 'infrastructure'
  | 'experiment'
  | 'abandoned_experiment'
  | 'uncategorized';

export type WorkstreamStatus =
  | 'active'
  | 'completed'
  | 'blocked'
  | 'uncertain'
  | 'research'
  | 'abandoned'
  | 'planning';

export type StationType =
  | 'session'
  | 'decision'
  | 'code_change'
  | 'problem'
  | 'milestone'
  | 'failure'
  | 'uncertainty'
  | 'blocker'
  | 'direction_change'
  | 'merge_point'
  | 'split_point'
  | 'plan_step';

export type StationStatus =
  | 'completed'
  | 'partial'
  | 'failed'
  | 'pending'
  | 'skipped';

export type LineTexture =
  | 'solid'
  | 'dashed'
  | 'blurred'
  | 'broken';

export type ModelTier =
  | 'sonnet'
  | 'haiku'
  | 'local_heuristic';

export type WorkstreamSource =
  | 'session'
  | 'external_folder';

// --- Supporting types ---

