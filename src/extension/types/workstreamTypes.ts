// --- Workstream ---

export interface Workstream {
  id: string;
  projectId: string;

  label: string;
  goal: string;
  type: WorkstreamType;
  status: WorkstreamStatus;

  sessionIds: string[];
  source?: WorkstreamSource;
  sourceFolderPath?: string;
  sourceFilePaths?: string[];
  sourceImportedAt?: string;
  sourceDocumentCount?: number;
  sourceTotalBytes?: number;
  relatedWorkstreamIds: string[];
  parentWorkstreamId?: string;
  childWorkstreamIds: string[];
  mergedIntoWorkstreamId?: string;

  confidence: number;
  confidenceReasons: string[];
  autoGenerated: boolean;
  userPinned: boolean;

  importanceScore: number;
  attentionScore: number;

  currentState: WorkstreamCurrentState;

  startedAt: string;
  lastActivityAt: string;
  completedAt?: string;
  lastViewedAt?: string;

  planId?: string;
  planReality?: PlanReality;

  metrics: WorkstreamMetrics;

  visual: WorkstreamVisualState;

  order: number;
}

// --- Station ---

export interface StationEvidence {
  kind: 'session_summary' | 'tool_use' | 'file_change' | 'test_result' | 'user_message' | 'assistant_message' | 'external_document';
  sessionId?: string;
  turnIndex?: number;
  text?: string;
  filePath?: string;
}

export interface StationVisualState {
  size: 'small' | 'medium' | 'large';
  glow: 'none' | 'recent' | 'attention' | 'resolved' | 'uncertain';
  labelVisible: boolean;
}

export interface Station {
  id: string;
  projectId: string;
  workstreamId: string;

  type: StationType;
  status: StationStatus;

  label: string;
  description: string;
  whyItMatters: string;

  sessionId?: string;
  turnIndex?: number;
  sourceFilePaths?: string[];

  order: number;
  timestamp: string;

  importanceScore: number;
  attentionScore: number;
  visibleInProjectMap: boolean;

  splitToWorkstreamIds?: string[];
  mergedFromWorkstreamIds?: string[];

  confidence: number;
  evidence: StationEvidence[];

  visual: StationVisualState;
}

// --- Plan reality model ---

